-- Alinear la secuencia de la identity con los IDs insertados a mano
SELECT setval(
    pg_get_serial_sequence('proyecto_filtro_funcionalidad', 'id'),
    GREATEST((SELECT MAX(id) FROM proyecto_filtro_funcionalidad), 1)
);

-- ----------------------------------------------------------------------------
-- Tabla filtro única
-- ----------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS proyecto_filtro (
    id                integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    project_id        integer NOT NULL REFERENCES project(project_id),
    funcionalidad_id  integer NOT NULL REFERENCES proyecto_filtro_funcionalidad(id),
    active            boolean NOT NULL DEFAULT true,
    created_at        timestamptz NOT NULL DEFAULT now(),
    CONSTRAINT ux_proyecto_filtro UNIQUE (project_id, funcionalidad_id)
);

-- ----------------------------------------------------------------------------
-- Seed: se reactiva Oficina Central (36) en project y se oculta explícitamente
-- en todas las funcionalidades excepto AC Actividades (la que la necesita).
-- ----------------------------------------------------------------------------
UPDATE project SET active = true WHERE project_id = 36 AND active = false;

-- Oculto en todas las funcionalidades menos AC_ACTIVIDADES
INSERT INTO proyecto_filtro (project_id, funcionalidad_id, active)
SELECT 36, fn.id, false
FROM proyecto_filtro_funcionalidad fn
WHERE fn.codigo <> 'AC_ACTIVIDADES'
ON CONFLICT (project_id, funcionalidad_id) DO NOTHING;

-- Visible en AC Actividades (fila explícita solo como documentación del caso;
-- sin fila también sería visible)
INSERT INTO proyecto_filtro (project_id, funcionalidad_id, active)
SELECT 36, fn.id, true
FROM proyecto_filtro_funcionalidad fn
WHERE fn.codigo = 'AC_ACTIVIDADES'
ON CONFLICT (project_id, funcionalidad_id) DO NOTHING;
