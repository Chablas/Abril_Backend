-- ============================================================================
-- Tabla filtro única de proyectos por funcionalidad (patrón lesson_area)
-- ============================================================================
-- project pasa a ser la tabla matriz/madre. Cada funcionalidad que lista
-- proyectos filtrando por project.active consulta ahora la tabla única
-- proyecto_filtro junto con el catálogo proyecto_filtro_funcionalidad:
--   - fila ausente          => el proyecto ES visible en esa funcionalidad
--   - fila con active=false => el proyecto NO aparece en esa funcionalidad
-- project.active sigue siendo el interruptor maestro global (si está en false,
-- el proyecto no aparece en ninguna funcionalidad, tenga o no fila filtro).
--
-- Los IDs del catálogo son fijos y se espejan en el backend en
-- Shared/Constants/ProyectoFiltroFuncionalidades.cs (mismo patrón que Roles).
--
-- Motivo: Oficina Central (project_id 36) estaba active=false y el commit
-- 55827e1f (18/07/2026) hizo que desapareciera del filtro de AC Actividades,
-- donde sí se necesita. Se reactiva en project y se oculta explícitamente en
-- las demás funcionalidades para no cambiarles el comportamiento.
-- Idempotente: se puede ejecutar más de una vez sin error.
-- ============================================================================

-- Limpieza de la versión anterior (13 tablas por funcionalidad); solo llegó a
-- aplicarse en dev, en producción estos DROP no hacen nada.
DROP TABLE IF EXISTS
    ac_actividad_proyecto_filtro,
    ac_observacion_proyecto_filtro,
    ac_revision_proyecto_filtro,
    hab_proyecto_filtro,
    shared_filters_proyecto_filtro,
    udp_cronograma_proyecto_filtro,
    udp_dashboard_proyecto_filtro,
    vecino_gestion_proyecto_filtro,
    vecino_croquis_proyecto_filtro,
    lesson_proyecto_filtro,
    residente_proyecto_filtro,
    ssoma_amonestacion_proyecto_filtro,
    ss_accidente_proyecto_filtro;

-- ----------------------------------------------------------------------------
-- Catálogo de funcionalidades (IDs fijos, espejo de las constantes del backend)
-- ----------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS proyecto_filtro_funcionalidad (
    id          integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    codigo      text NOT NULL,
    nombre      text NOT NULL,
    created_at  timestamptz NOT NULL DEFAULT now(),
    CONSTRAINT ux_proyecto_filtro_funcionalidad_codigo UNIQUE (codigo)
);

